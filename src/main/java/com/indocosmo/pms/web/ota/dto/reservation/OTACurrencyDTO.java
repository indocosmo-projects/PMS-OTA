package com.indocosmo.pms.web.ota.dto.reservation;

public class OTACurrencyDTO {

	private int id;
	
	private String currencyid;
	
	private String country;
	
	private String currency;
	
	private String currencycode;
	
	private String sign;
	
	private String digitsafterdecimal;
	
	private String isbasecurrency;
	
	private String exchangerate;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCurrencyid() {
		return currencyid;
	}

	public void setCurrencyid(String currencyid) {
		this.currencyid = currencyid;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	public String getCurrencycode() {
		return currencycode;
	}

	public void setCurrencycode(String currencycode) {
		this.currencycode = currencycode;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getDigitsafterdecimal() {
		return digitsafterdecimal;
	}

	public void setDigitsafterdecimal(String digitsafterdecimal) {
		this.digitsafterdecimal = digitsafterdecimal;
	}

	public String getIsbasecurrency() {
		return isbasecurrency;
	}

	public void setIsbasecurrency(String isbasecurrency) {
		this.isbasecurrency = isbasecurrency;
	}

	public String getExchangerate() {
		return exchangerate;
	}

	public void setExchangerate(String exchangerate) {
		this.exchangerate = exchangerate;
	}
	
	
	
}
